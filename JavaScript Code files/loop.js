//While loop
let i = -1
while(i<4)
{
    i++
    console.log(`While Loop ${i}`);
} 
console.log("While Loop End")
console.log(" ")


//Do While

let k = 0;

do {
    console.log(`Do While Loop ${k}`);
    k++;
} while (k < 5);

console.log("Do While Loop ended.")
console.log(" ")


//For Loop
for(let j=0;j<5;j++)
{
    console.log(`For Loop ${j}`)
}
console.log("For Loop ended.")
console.log(" ")